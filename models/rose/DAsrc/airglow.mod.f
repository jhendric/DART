      subroutine oh_ver( tn, c_o, c_o2, c_n2, c_o3, c_h, v_oh, e_v_oh )
!------------------------------------------------------------------
!
!   Routine to model OH meinel volume emission rate at 2.0e-6m
!   in photons/cm3/s and ergs/cm3/s. Emission comprises OH*(9-7) and OH*(8-6)
!
!------------------------------------------------------------------

      use params, only: nx, ny, nz
      use chem, only: a2 

      implicit none

      real, dimension(nz, nx, ny), intent(in)  ::  tn, c_o2, c_o, c_n2
      real, dimension(nz, nx, ny), intent(in)  ::  c_o3, c_h
      real, dimension(nz, nx, ny), intent(out) ::  v_oh, e_v_oh 

! branching ratios for H+O3 -> OH*

      real, parameter :: f9=0.4444, f8=0.2756

! Einstein coefficients

      real, parameter :: a9 = 215.05
      real, parameter :: a98 = 20.5
      real, parameter :: a97 = 118.35

      real, parameter :: a8 = 178.06
      real, parameter :: a86 = 117.21

! emission line parameters

      real, parameter :: wn97 = 4998.     ! wavenumber of 9-7 emission (cm-1)
      real, parameter :: wn86 = 5332.     ! wavenumber of 8-6 emission (cm-1)

      real, parameter :: wn2erg = 1.986445e-16  ! 1 cm-1 in ergs
      
      real, parameter :: e97 = wn97 * wn2erg
      real, parameter :: e86 = wn86 * wn2erg

! collisional quenching rate constants OH* + N2 or OH* + O2

      real, parameter :: k9o2 = 300.e-13
      real, parameter :: k9n2 = 4.e-13
      real, parameter :: k98o2 = 42.e-13
      real, parameter :: k98n2 = 4.e-13

      real, parameter :: k8o2 = 119.e-13
      real, parameter :: k8n2 = 7.e-13

! local variables

      real, dimension(nz, nx, ny) :: c9, c8, c98
      real, dimension(nz, nx, ny) :: k1, l1, l8, l9 
      real, dimension(nz, nx, ny) :: n9, n8 
      real, dimension(nz, nx, ny) :: prod 

!... production rate for OH* = a2 * [H] * [O3]

      prod(:,:,:) = a2(:,:,:) *  c_h(:,:,:) * c_o3(:,:,:)

!... collisional quenching rates for OH*(v=9) and OH*(v=8)

      c9  = k9o2  * c_o2(:,:,:) + k9n2  * c_n2(:,:,:)
      c8  = k8o2  * c_o2(:,:,:) + k8n2  * c_n2(:,:,:)
      c98 = k98o2 * c_o2(:,:,:) + k98n2 * c_n2(:,:,:)

!... chemical loss rate for OH* through reaction w/ atomic oxygen

      k1(:,:,:) = 2.2e-11 * exp(120./tn(:,:,:))
      l1(:,:,:) = k1(:,:,:) * c_o(:,:,:)

!... total loss rates for OH*(v=9) and OH*(v=8) including radiative loss

      l9(:,:,:) = a9 + c9(:,:,:) + l1(:,:,:)
      l8(:,:,:) = a8 + c8(:,:,:) + l1(:,:,:)

!... calc. number densities for OH*(v=9) and OH*(v=8)

      n9(:,:,:) = f9 * prod(:,:,:) / l9(:,:,:)
      
      n8(:,:,:) = (f8 + f9 * (c98(:,:,:) + a98) / l9(:,:,:) )
     $          * prod(:,:,:) / l8(:,:,:) 

!... total volume emission rate

      v_oh(:,:,:) = a97 * n9(:,:,:) + a86 * n8(:,:,:)
      
      e_v_oh(:,:,:) = e97 * a97 * n9(:,:,:) + e86 * a86 * n8(:,:,:)

      return

      end subroutine oh_ver

!------------------------------------------------------------------
      subroutine e5577( tn, c_o, c_o2, c_n2, v5577 )
!------------------------------------------------------------------
!
!   Calculate greenline emission due to atomic oxygen recombination
!
!------------------------------------------------------------------
! 
!   The greenline volume emission rates due to the recombination
!   source, which dominates in the 85 to 110 km region at night.
!   (from Ian McDade, (mcdade@windic.yorku.ca) Mar 7, 1994 mail 
!   communication to Roble)
!
!                    A5 k1 [O]^3 {[N2]+[O2]}
!           V1S = -----------------------------   (ph cm-3 sec-1)
!                 {A6 + k5[O2]} {C'[O2] + C"[O]} 
!
!   where [O], [O2] and [N2] are number densities cm-3
!
!   A5 = 1.18 sec-1
!   A6 = 1.35 sec-1
!   k1 = 4.7E-33 (300/T)^2  cm6 sec-1
!   k5 = 4.0E-12 exp(-865/T) cm3 sec-1
!   C' = 15 dimensionless
!   C" = 211 dimensionless
!
!------------------------------------------------------------------

      use params

      implicit none

      real, dimension(nz, nx, ny), intent(in)  ::  tn, c_o2, c_o, c_n2
      real, dimension(nz, nx, ny), intent(out) ::  v5577

      real, parameter :: a5 = 1.18, a6 = 1.35, cp = 15., cpp = 211.

      real, dimension(nz, nx, ny) :: rk1, rk5

      rk1 = 4.7e-33 * (300./tn)**2
      rk5 = 4.0e-12 * exp(-865./tn)

      v5577 = (a5 * rk1 * c_o**3 * (c_n2 + c_o2)) /
     +      ((a6 + rk5 * c_o2) * (cp * c_o2 + cpp * c_o))

      return

      end subroutine e5577

!------------------------------------------------------------------
      subroutine eo200( tn, c_o, c_o2, c_n2, vo200 )
!------------------------------------------------------------------
!
!   O2 (0-0) band nightglow emission
!
!------------------------------------------------------------------
!
!   Given tn, xo2,xo,xn2 (species in cm-3) return O2 (0-0) band
!   emission (from Ian McDade, (mcdade@windic.yorku.ca) Mar 7,
!   1994 mail communication to Roble)
!
!   The nighttime O2 Atmospheric (0-0) band emission rates in
!   the region may be similarly modelled using:
!
!               A1 k1 [O]^2 {[N2]+[O2]} [O2]
!   Vat = -------------------------------------------------- (ph cm-3 sec-1)
!         {A2 + k2a[O2] + k2b[N2] + k2c[O]} {D'[O2] + D"[O]} 
!
!   where [O], [O2] and [N2] are number densities cm-3
!   A1 = 0.079 sec-1
!   A2 = 0.083 sec-1
!   k1 = 4.7E-33 (300/T)^2  cm6 sec-1
!   k2a = 4.0E-17 cm3 sec-1
!   k2b = 2.2E-15 cm3 sec-1
!   k2c = 8.0E-14 cm3 sec-1
!   D' = 6.6 dimensionless
!   D" = 19  dimensionless
!
!------------------------------------------------------------------

      use params

      implicit none

      real, dimension(nz, nx, ny), intent(in)  ::  tn, c_o2, c_o, c_n2
      real, dimension(nz, nx, ny), intent(out) ::  vo200

      real, parameter :: a1 = .079, a2 = .083, rk2a = 4.e-17,
     +          rk2b=2.2e-15,  rk2c=8.e-14, dp=6.6, dpp=19.

      real, dimension(nz, nx, ny) :: rk1

      rk1 = 4.7e-33 * (300./tn)**2

      vo200 = ( a1 * rk1 * c_o**2 * ( c_n2 + c_o2 ) * c_o2 ) /
     +        ( (a2 + rk2a * c_o2 + rk2b * c_n2 + rk2c * c_o ) *
     +          ( dp * c_o2 + dpp * c_o ) )
      
      return

      end subroutine eo200

!------------------------------------------------------------------
      subroutine eoh83( tn, c_o, c_o2, c_n2, voh83 )
!------------------------------------------------------------------
!
!   OH emission v(8,3) band
!
!------------------------------------------------------------------

      use params
      
      implicit none

      real, dimension(nz, nx, ny), intent(in)  ::  tn, c_o, c_o2, c_n2
      real, dimension(nz, nx, ny), intent(out) ::  voh83

      real, parameter :: f8 = 0.29
      real, dimension(nz, nx, ny) :: pk6n2, pk6o2
      
      pk6n2 = 5.70e-34 * (300./tn)**2.62
      pk6o2 = 5.96e-34 * (300./tn)**2.37

      voh83 = f8 * c_o * c_o2 * ( pk6n2 * c_n2 + pk6o2 * c_o2) /
     +        ( 260. + 2.e-11 * c_o2 )
     
      return

      end subroutine eoh83
     

