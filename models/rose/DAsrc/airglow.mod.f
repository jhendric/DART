      subroutine aband_ver(tn, press, xo2, xo, xn2, xo1d, nz, secsza,
     +  ieo200, fout)
!------------------------------------------------------------------
!
!   Calculate Atmospheric Band O2(b1-Sigma -> X3-Sigma)(0-0) emission:
!
!   Up to three components can be requested:
!
!   ieo200(1) > 0 -> Production via Barth-type 2-step process:
!                    O + O + M -> O2* + M
!                    O2* + O2 -> O2(b1-Sigma) + O2
!
!   ieo200(2) > 0 -> O2(b1-Sigma) production via resonance scattering
!                    O2 + hv -> O2(b1-Sigma)
!
!   ieo200(3) > 0 -> O2(b1-Sigma) production via collisional excitation
!                    with O(1D) 
!
!------------------------------------------------------------------
!   Change history:
!
! 8/29/00:  Added dayglow mechanisms - D.Marsh (marsh@ucar.edu)
!
!------------------------------------------------------------------

      implicit none

      integer, intent(in) :: nz 
      real, dimension(nz), intent(in) :: tn, press 
      real, dimension(nz), intent(in) :: xo2, xo, xn2, xo1d
      real, intent(in) :: secsza                       ! secant of zenith angle
      integer, dimension(3), intent(in) :: ieo200      ! flag for night/dayglow

      real, dimension(nz), intent(out) :: fout         ! O2(b1-Sigma) VER

!... parameters 
      real, parameter :: a1s =.085
      real, parameter :: fc =.93
      real, parameter :: phi =.95                 ! Green, 2000
      real, parameter :: k2a=3.9e-17, k2b=2.1e-15 ! JPL-97

      real, parameter :: a1=.079, a2=.083
      real, parameter :: rk2a=4.e-17, rk2b=2.2e-15, rk2c=8.e-14
      real, parameter :: dp=6.6, dpp=19.

!... locals
      integer :: k
      real :: rk1
      real, dimension(nz) :: k0, q, pro1d, p
      real :: tbar(nz), o2b(nz)
      real :: gf(nz)

      fout = 0.0
 
!... ieo200(1) > 0 -> Production via Barth-type 2-step process
!   
!   3/9/94: from Ian McDade, (mcdade@windic.yorku.ca) Mar 7, 1994 mail 
!   communication to Roble
!
!   The nighttime O2 Atmospheric (0-0) band emission rates
!   (photons cm-3 sec-1) in the region may be modelled using:
!
!                     A1 k1 [O]^2 {[N2]+[O2]} [O2]
!   Vbarth= --------------------------------------------------
!           {A2 + k2a[O2] + k2b[N2] + k2c[O]} {D'[O2] + D"[O]} 
!
!   where [O], [O2] and [N2] are number densities cm-3
!   A1 = 0.079 sec-1
!   A2 = 0.083 sec-1
!   k1 = 4.7E-33 (300/T)^2  cm6 sec-1
!   k2a = 4.0E-17 cm3 sec-1
!   k2b = 2.2E-15 cm3 sec-1
!   k2c = 8.0E-14 cm3 sec-1
!   D' = 6.6 dimensionless
!   D" = 19  dimensionless
!
!   ref: Planet. Space Sci., 34, 789-800, 1986.
!
      if (ieo200(1).gt.0) then
        do k = 1, nz
          rk1 = 4.7e-33*(300./tn(k))**2

          fout(k) = a1*rk1*xo(k)**2 * 
     +      ((xn2(k)+xo2(k)) / (dp*xo2(k)+dpp*xo(k))) *
     +      ( xo2(k) / (a2 + rk2a*xo2(k) + rk2b*xn2(k) + rk2c*xo(k)) )

!          fout(k) = (a1*rk1*xo(k)**2 * (xn2(k)+xo2(k)) * xo2(k)) /
!     +      ((a2 + rk2a*xo2(k) + rk2b*xn2(k) + rk2c*xo(k)) *
!     +      (dp*xo2(k)+dpp*xo(k)))

        enddo
      endif

!... ieo200(2) > 0 -> O2(b1-Sigma) production via resonance scattering
!
!    code supplied by M.Mlynczak
!
    
      gf = 0.0
      if ((ieo200(2).gt.0).and.(secsza .le. 50)) then

        ! calculate average temperature and optical mass
        call l13( press, tn, nz, secsza, tbar, o2b )

        ! calculate g-factor
        call gfact(tbar, o2b, nz, gf)
        gf = gf * 6.192e-9

      endif
 
!... ieo200(3) > 0 -> O2(b1-Sigma) production via collisional excitation
!                    with O(1D) 
!         phi = 0.95 (efficiency for production of b1-sigma)
!         (ref: Green, Shi and Barker, J Phys Chem 104, 6218, 2000)
!         k0 =  3.2(-11)exp(70/T) cm3 s-1 (rate for O(1D) + O2 from JPL97)

      pro1d = 0.0
      if (ieo200(3).gt.0) then

        k0 =  3.2e-11 * exp(70./tn)
        pro1d = phi * k0 * xo1d 

      endif
 
!... calculate total band VER (photons cm-3 sec-1):
!
!    Vat = Fc Q { g + pro1d } * xo2 + Vbarth
!   
!    where, Fc =  0.93  ( Franck-Condon factor for 0-0 )
!    and Q is the ratio of radiative losses to all losses
!
!          Q =        A1s
!              -----------------------
!              A1s + k2a[O2] + k2b[N2] 

      if (ieo200(2).gt.0 .or. ieo200(3).gt.0) then 

        q = a1s / ( a1s + k2a*xo2 + k2b*xn2 )
        fout = fc * q * ( pro1d + gf ) * xo2 + fout
      endif

      return
      end subroutine aband_ver 


!----------------------------------------------------------------------
      subroutine l13( pmb, t, nz, secsza, tbar, o2b )
!----------------------------------------------------------------------
!
!  program to calculate average temperature and o2 optical mass amounts
!  for input into the line-by-line program.
!  note: uses fixed o2 vmr (.21)
! 
!----------------------------------------------------------------------

      implicit none

      integer, intent(in) :: nz
      real, dimension(nz), intent(in) :: pmb,  ! presssure (mbars)
     $                                   t     ! neutral temperature
      real, intent(in) :: secsza

      real, dimension(nz), intent(out) :: tbar, o2b

!... locals
      real, parameter :: k1 = 2.119e22     ! R/(g*kboltz)
                                           ! in mbar-1cm-2 
      integer :: i
        
!... calculate optical mass exactly (see notes 2-18-93)
	do i=1,nz-1
	  o2b(i)=0.21 * k1 * (pmb(i)-pmb(i+1))
	  o2b(i)= o2b(i) * secsza
	enddo
	o2b(nz) =0.21 * k1 * pmb(nz) * secsza

!... calculate mean temperatures
	do i=1,nz-1
	  tbar(i)=(t(i)+t(i+1))/2.
	enddo
        tbar(nz) = t(nz)

	return

	end subroutine l13
	
!----------------------------------------------------------------------
	subroutine gfact(tbar, o2b, nz, gf)
!----------------------------------------------------------------------
!
!  routine to calculate g/g_infinity
!
!----------------------------------------------------------------------

	use egadb2, only : n, utab, etab, tk

	implicit none

	integer, intent(in) :: nz
	real, dimension(nz), intent(in) :: tbar  ! mean layer temperature
	real, dimension(nz), intent(in) :: o2b   ! o2 optical mass
	real, intent(out) :: gf(nz)              ! g/g_infinity

!... parameters 
!       real, parameter :: sb=1.946e-22      ! HITRAN 86 bandstrength
	real, parameter :: sb=2.244e-22      ! HITRAN 96 bandstrength

!... locals
	real :: u, us, utot 
	real :: e1, e2, a1, w1, w2, u1, u2 
	real :: eint(n)
	integer :: i, ij, jlo, jt

!... do the emission calculations
	jlo=1
	utot=0.
	e1=0.
	e2=0.

!... loop over altitude
	do i=nz,1,-1

!... use linear interpolation to generate emissivity table (eint)
!    from etab at mean temperature (tbar)

	  if (tbar(i) .gt. 300) then
	    eint(:)=etab(:,7)
	    print *, 'tbar: ', tbar(i)
	  else if (tbar(i) .lt. 150) then
	    eint(:)=etab(:,1)
	    print *, 'tbar: ', tbar(i)
	  else
	    jt=1
	    call locate(tk,7,tbar(i),jt)
	    a1=(tbar(i)-tk(jt))/25.
	    do ij=1,n
	      eint(ij)=etab(ij,jt)+a1*(etab(ij,jt+1)-etab(ij,jt))
	    enddo
	  endif

	  u = o2b(i)

!... calculate psuedo mass

	  if(e1.eq.0.) then
	    us=0.
	  else
  	    jlo=1
!... find location of e1 in etab array 
	    call locate(eint,n,e1,jlo)
	    if (jlo .lt. n) then
	      u1=utab(jlo)
	      u2=utab(jlo+1)
	      w1=eint(jlo)
	      w2=eint(jlo+1)
	      a1=(e1-w1)/(w2-w1)
!... extract column for that location 
	      us=u1+a1*(u2-u1)
	    else
              us = utab(jlo)
	    endif
	  endif

!... add on column increment to previous column
	  utot=u+us

!... now get e2 from utot
	  jlo=1
	  call locate(utab,n,utot,jlo)

	  if (jlo .lt. n) then
	    a1=(utot-utab(jlo))/(utab(jlo+1)-utab(jlo))
	    e2=a1*(eint(jlo+1)-eint(jlo))+eint(jlo)
	  else 
            e2 = eint(jlo)
	  endif
	    gf(i)=(e2-e1)/u/sb
	  e1=e2
	enddo

	return

	end subroutine gfact 

!----------------------------------------------------------------------
      subroutine locate(xx,n,x,j)
!----------------------------------------------------------------------

      integer :: n
      real :: xx(n)
      real :: x 
      integer :: j

      integer :: jl, ju, jm

      jl=0
      ju=n+1

10    if(ju-jl.gt.1)then
        jm=(ju+jl)/2
        if((xx(n).gt.xx(1)).eqv.(x.gt.xx(jm)))then
          jl=jm
        else
          ju=jm
        endif
      go to 10
      endif

      j=jl

      return

      end subroutine locate

