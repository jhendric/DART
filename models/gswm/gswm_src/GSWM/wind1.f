      SUBROUTINE wind1(t,z,mon,uu,vv,ww,fv,fn2w)
c
c     This subroutine reconstructs u,v,w,fv and
c     n2w from tables of legendre polynomial coefficients
c     of the harmonics of u,v,and w.  
c
c     Single points in latitude and altitude are output
c
c     t         latitude (90N to -90S)
c     z         altitude (km)
c     mon       month
c     u         zonal wind
c     v         meridional wind
c     w         vertical wind
c     fv        coriolis acceleration
c     fn2w       adiabatic heating
c    
c     v is positive northward
c.....................................................
c  Main parameters 
      parameter(zmin=70,zmax=110)
c  Parameters for input data
      parameter(nterm=7,nharm=5,nalt=21)
c....................................................
c  zmin exp smoothed 60-70km, all fields 0 below 60km
c  zmax exp smoothed 110-120km, all fields 0 above 120km
c
c  SWITCH settings:
c  n1=
c     1  zonal
c     2  meridional
c     3  vertical
c     4  fv
c     5  fn2w
c.....................................................

      real ew(nterm,nharm,nalt),ns(nterm,nharm,nalt)
      real w(nterm,nharm,nalt)
      real yn2w(nalt),yo
      dimension y(nterm,nharm,nalt)
      common /wvcom/ ew,ns,w

      do n1=1,5
        call rddata()
        call switch(y,n1)

        fmon=float(mon)
        if(n1.eq.5) then
          call mkn2w(y,yn2w,t,fmon)
        endif
        call menu(y,yo,yn2w,t,ixz,z,n1,
     &    iharm,fmon)
        if(n1.eq.1) then
            uu=yo
            endif
        if(n1.eq.2) then
            vv=yo
            endif
        if(n1.eq.3) then
            ww=yo
            endif
        if(n1.eq.4) then
            fv=yo
            endif
        if(n1.eq.5) then
            fn2w=yo
            endif
      enddo
      return
      end

c-------------------------------------------------
      SUBROUTINE menu(y,yo,yn2w,t,ixz,z,n1,
     &  iharm,fmon)
c  Switches to the correct subroutine
      parameter(nterm=7,nharm=5,nalt=21)

      dimension yn2w(nalt)
      dimension y(nterm,nharm,nalt)

      if(n1.ne.5) then
        call mkmon(y,yo,t,z,fmon)
        if(n1.eq.4) then
          call fv(yo,t)
        endif
      endif

      if(n1.eq.5) then
        call intn2w(yo,yn2w,t,z)
      endif

      return
      end

c---------------------------------------------------
      SUBROUTINE mkmon(y,yo,t,z,fmon)
c  Creates monthly wind profile from harmonics

      parameter(nterm=7,nharm=5,nalt=21)
      parameter(altmin=70.,altmax=110.)
      parameter(altbot=60.,alttop=120.)
      
      real yh(nharm)
      dimension y(nterm,nharm,nalt)

      if(z.lt.altbot.or.z.gt.alttop)then
        yo=0.
      else

       PI = acos(-1.)
       w = PI/6.

       do ixharm=1,nharm
        if(z.lt.altmin)then
          call mkharm(y,yo,t,altmin,ixharm)
        else
          if(z.gt.altmax)then
            call mkharm(y,yo,t,altmax,ixharm)
          else
            call mkharm(y,yo,t,z,ixharm)
          endif
        endif
        yh(ixharm)=yo
       enddo

        yo=yh(1)+(yh(2)*cos(w*fmon))+
     &    (yh(3)*sin(w*fmon))+(yh(4)*cos(2*w*fmon))+
     &    (yh(5)*sin(2*w*fmon))       

       if(z.lt.altmin) then
          yo=yo*exp((z-altmin)/4)
       endif
       if(z.gt.altmax) then
          yo=yo*exp((altmax-z)/4)
       endif
      endif
      return 
      end

c----------------------------------------------------
      SUBROUTINE mkharm (y,yo,t,z,iharm)
c  Creates harmonics from data tables

      parameter(nterm=7,nharm=5,nalt=21)
      parameter(altmin=70.,altmax=110.)

      real p(nterm)
      dimension y(nterm,nharm,nalt)
      real yp
      doubleprecision cosine,tt

      yo=0.
      yp=0.
      tt=t

      if(z.ge.altmin.and.z.le.altmax)then

           cosine= dcos(0.01745329252 * (tt+90.))
           p(1)= 1.
           p(2)= cosine
           do 36 l=2,nterm-1
             fl= l
 36          p(l+1)=((2.*fl-1.)*cosine*p(l)-(fl-1.)
     &       *p(l-1))/fl

           x=z-altmin
           ixz=(x/2)+1

        do 115 j= 1, nterm
 115      yo= yo+y(j,iharm,ixz)*p(j)

        if (mod(x,2.).ne.0) then
          do 116 j= 1, nterm
 116      yp= yp+y(j,iharm,ixz+1)*p(j)
          yo=((((x-(int(x)/2)*2.))/2.)*(yp-yo))+yo
     
        endif
      endif

      return
      end

c-------------------------------------------------------
      SUBROUTINE fv(yo,t)
c  Computes fv based on reconstructed monthly v

      parameter(nterm=7,nharm=5,nalt=21)      

      OMG=7.2921E-05
      PI=acos(-1.)

      yo=-2.0*OMG*sin(t*PI/180.)*yo

      return
      end

c-------------------------------------------------------
      SUBROUTINE mkn2w(y,yn2w,tt,fmon)
c  Computes n2w based on reconstructed monthly w

      parameter(nterm=7,nharm=5,nalt=21)
      parameter(altmin=70.,dalt=2.)

      dimension y(nterm,nharm,nalt)
      dimension yn2w(nalt),ytemp(nalt)
      real T(nalt),EN2(nalt)

      do ixalt=1,nalt
        alt=altmin+(ixalt-1)*dalt
        call mkmon(y,yo,tt,alt,fmon)
          ytemp(ixalt)=yo
      enddo

      DATA T/206.0, 203.0, 204.0, 200.0, 202.0, 200.0, 201.0,
     &       196.0, 191.0, 186.0, 183.0, 186.0, 190.0, 197.0,
     &       203.0, 209.0, 211.0, 218.0, 224.0, 236.0, 248.0/

      N=nalt
      G=9.8
      GAM=1.4
      GMD=0.98/100.0
      DZ=2.0E+03

      DO 310 I=2,N
       IF(I.EQ.N) GO TO 312
         GM=(T(I+1)-T(I-1))/2.0/DZ
        GO TO 314
 312        GM=(T(N)-T(N-1))/DZ
 314            EN2(I)=G*(GMD-GM)/T(I)
 310             CONTINUE
      DO 318 I=2,N
         yn2w(I)=-EN2(I)*ytemp(I)*T(I)*GAM/980.0
     &     *86400.0
 318  CONTINUE

      return
      end

c----------------------------------------------------------
      SUBROUTINE intn2w(yo,yn2w,t,z)
c Interpolate altitudes for n2w

      parameter(nterm=7,nharm=5,nalt=21)
      parameter(altmin=70.,altmax=110.)
      parameter(altbot=60.,alttop=120.)

      dimension yn2w(nalt)
      real yp

      yo=0.
      yp=0.

      if(z.ge.altbot.and.z.le.alttop)then
       if(z.lt.altmin)then
           yo=yn2w(1)*exp((z-altmin)/4)
        else 
         if(z.gt.altmax)then
             yo=yn2w(nalt)*exp((altmax-z)/4)
         else
           x=z-altmin
           ixz=(x/2)+1
           yo= yo+yn2w(ixz)

           if (mod(x,2.).ne.0) then
             do j= 1, nterm
                 yp= yn2w(ixz+1)
             enddo
 
               yo=((((x-(int(x)/2)*2.))/2.)
     &           *(yp-yo))+yo
           endif
         endif
       endif
      endif

      return
      end

c----------------------------------------------------------
      SUBROUTINE switch(y,n1)
c  Switches to the correct wind profile (u,v,w) in the tables

      parameter(nterm=7,nharm=5,nalt=21)

      real ew(nterm,nharm,nalt),ns(nterm,nharm,nalt)
      real w(nterm,nharm,nalt),y
      dimension y(nterm,nharm,nalt)
      common /wvcom/ ew,ns,w
      
      if(n1.eq.1) then
        do 5 i=1,nterm
        do 5 j=1,nharm
        do 5 k=1,nalt   
 5      y(i,j,k)=ew(i,j,k)
      endif
      if(n1.eq.2.or.n1.eq.4) then
        do 10 i=1,nterm
        do 10 j=1,nharm
        do 10 k=1,nalt
 10     y(i,j,k)=ns(i,j,k)
      endif
      if(n1.eq.3.or.n1.eq.5) then
        do 15 i=1,nterm
        do 15 j=1,nharm
        do 15 k=1,nalt
 15     y(i,j,k)=w(i,j,k)
      endif
     
      return
      end

