  SUBROUTINE lubksb(a,n,np,indx,b)

! Performs a back substitution on a with ops in indx from ludcmp with result
! returned in vector b

! Arguments
        integer, intent(in) :: n,np
        integer, dimension(:), intent(in) :: indx
	real, dimension(:,:), intent(in) :: a
	real, dimension(:), intent(inout) :: b

! Local
      INTEGER i,ii,j,ll
      REAL sum
      ii=0
      do 12 i=1,n
        ll=indx(i)
        sum=b(ll)
        b(ll)=b(i)
        if (ii.ne.0)then
          do 11 j=ii,i-1
            sum=sum-a(i,j)*b(j)
11        continue
        else if (sum.ne.0.) then
          ii=i
        endif
        b(i)=sum
12    continue
      do 14 i=n,1,-1
        sum=b(i)
        do 13 j=i+1,n
          sum=sum-a(i,j)*b(j)
13      continue
        b(i)=sum/a(i,i)
14    continue
      return
      END subroutine lubksb


      SUBROUTINE ludcmp(a,n,np,indx,d)

! Performs an LU decomposition of array a, returns the index of row ops in indx
! Arguments
        integer, intent(in) :: n,np
        integer, dimension(:), intent(out) :: indx
	real, intent(out) :: d
	real, dimension(:,:), intent(inout) :: a 

! Local
        integer, parameter :: NMAX=500
      	real, parameter :: TINY=1.0e-20
        integer :: i,imax,j,k
        real :: aamax,dum,sum
	real, dimension(NMAX) :: vv

      d=1.
      do 12 i=1,n
        aamax=0.
        do 11 j=1,n
          if (abs(a(i,j)).gt.aamax) aamax=abs(a(i,j))
11      continue
        if (aamax.eq.0.) pause 'singular matrix in ludcmp'
        vv(i)=1./aamax
12    continue
      do 19 j=1,n
        do 14 i=1,j-1
          sum=a(i,j)
          do 13 k=1,i-1
            sum=sum-a(i,k)*a(k,j)
13        continue
          a(i,j)=sum
14      continue
        aamax=0.
        do 16 i=j,n
          sum=a(i,j)
          do 15 k=1,j-1
            sum=sum-a(i,k)*a(k,j)
15        continue
          a(i,j)=sum
          dum=vv(i)*abs(sum)
          if (dum.ge.aamax) then
            imax=i
            aamax=dum
          endif
16      continue
        if (j.ne.imax)then
          do 17 k=1,n
            dum=a(imax,k)
            a(imax,k)=a(j,k)
            a(j,k)=dum
17        continue
          d=-d
          vv(imax)=vv(j)
        endif
        indx(j)=imax
        if(a(j,j).eq.0.)a(j,j)=TINY
        if(j.ne.n)then
          dum=1./a(j,j)
          do 18 i=j+1,n
            a(i,j)=a(i,j)*dum
18        continue
        endif
19    continue
      return
      END subroutine ludcmp


      REAL FUNCTION ran1(idum)
      implicit none

      INTEGER, intent(inout) :: idum
      INTEGER, parameter :: IA=16807,IM=2147483647,IQ=127773
      INTEGER, parameter :: IR=2836,NTAB=32,NDIV=1+(IM-1)/NTAB
      REAL, parameter :: AM=1./IM,EPS=1.2e-7,RNMX=1.-EPS

      INTEGER :: j,k,iy
      INTEGER, dimension(:) :: iv(NTAB)

      SAVE iv,iy
      DATA iv /NTAB*0/, iy /0/

      if ((idum.le.0).or.(iy.eq.0)) then
        idum=max(-idum,1)
        do 11 j=NTAB+8,1,-1
          k=idum/IQ
          idum=IA*(idum-k*IQ)-IR*k
          if (idum.lt.0) idum=idum+IM
          if (j.le.NTAB) iv(j)=idum
11      continue
        iy=iv(1)
      endif
      k=idum/IQ
      idum=IA*(idum-k*IQ)-IR*k
      if (idum.lt.0) idum=idum+IM
      j=1+iy/NDIV
      iy=iv(j)
      iv(j)=idum
      ran1=min(AM*iy,RNMX)
      return
      END function ran1

      real FUNCTION gasdev(idum)
      implicit none

      INTEGER, intent(inout) :: idum
!U    USES ran1
      INTEGER :: iset
      REAL :: fac,gset,rsq,v1,v2

      SAVE iset,gset
      DATA iset/0/
      if (iset.eq.0) then
1       v1=2.*ran1(idum)-1.
        v2=2.*ran1(idum)-1.
        rsq=v1**2+v2**2
        if((rsq.ge.1.).or.(rsq.eq.0.))goto 1
        fac=sqrt(-2.*log(rsq)/rsq)
        gset=v1*fac
        gasdev=v2*fac
        iset=1
      else
        gasdev=gset
        iset=0
      endif
      return
      END function gasdev

